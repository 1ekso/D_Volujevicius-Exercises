let bitNumberOne = 10;
let bitNumberTwo = 20;
console.log(bitNumberOne & bitNumberTwo); // 0
console.log(bitNumberOne | bitNumberTwo); // 30
console.log(~bitNumberTwo); // -21
console.log(bitNumberOne ^ bitNumberTwo); // 30
console.log(bitNumberOne << 1); // 20
console.log(bitNumberOne >> 1); // 5
console.log(bitNumberOne >>> 1); // 5